    private void equalsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalsButtonActionPerformed
        
        if(operator.equals("") || resultsLabel.getText().equals("") || currentEquationLabel.getText().equals("") 
                || currentEquationLabel.getText().endsWith(".") || resultsLabel.getText().equals("")
                || resultsLabel.getText().endsWith(".")){
            
        }
        else{
            num2 = Double.parseDouble(resultsLabel.getText());

            // Doing the calculation
            reg.calculation(num1, operator, num2);

            // Get the result
            result = reg.getCalculation();

            // Display the result in the resultsLabel
            resultsLabel.setText("" + result);
            
            // Place the equation and result into the history table
            if(stateChanged == false && num2 >= 0){
                DefaultTableModel table = (DefaultTableModel) resultsTable.getModel();
                Object storedEquation[] = {num1 + " " + operator + " " + num2, result};
                table.addRow(storedEquation);
            }
            else{
                DefaultTableModel table = (DefaultTableModel) resultsTable.getModel();
                Object storedEquation[] = {num1 + " " + operator + " " + " (" + num2 + ")", result};
                table.addRow(storedEquation);
            }
            decimalEntered = true;
            
        }
                              
    }//GEN-LAST:event_equalsButtonActionPerformed

