    private void changeStateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeStateButtonActionPerformed
        if(resultsLabel.getText().equals("")){
            
        }
        else{
            if(num1Entered == false){
                double changeSign = Double.parseDouble(resultsLabel.getText()) * -1;
                resultsLabel.setText("" + changeSign);
                currentEquationLabel.setText("" + changeSign);
                decimalEntered = true;
            }
            else{
                    double changeSign = Double.parseDouble(resultsLabel.getText()) * -1;
                    if(changeSign < 0){
                        resultsLabel.setText("" + changeSign);
                        currentEquationLabel.setText(num1 + " " + operator + " (" + resultsLabel.getText() + ")");
                    }
                    else{
                        resultsLabel.setText("" + changeSign);
                        currentEquationLabel.setText(num1 + " " + operator + " " + resultsLabel.getText());
                    }
            }
        }
        
    }//GEN-LAST:event_changeStateButtonActionPerformed

