    private void phoneAFriendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_phoneAFriendButtonActionPerformed
        
        PhoneAFriend phone = new PhoneAFriend();
        
        // Randomly select if the user is calling his/her smart friend, semi-smart
        // friend, or... that dumb friend that'll screw them over.
        // This is just to add an extra challenge to the game.
        Random friend = new Random();
        int pickAFriend = friend.nextInt(3) + 1;
        
        switch(pickAFriend){
            case 1:
                phoneAFriend = phone.phoneSmartFriend(setUpAnswers);
                phonedFriendLBL.setText("Your super smart friend picked up their phone!");
                break;
            case 2:
                phoneAFriend = phone.phoneSemiSmartFriend(setUpAnswers);
                phonedFriendLBL.setText("Your semi-smart friend picked up their phone.");
                break;
            case 3:
                phoneAFriend = phone.phoneDumbFriend(setUpAnswers);
                phonedFriendLBL.setText("Your dumb friend picked up the phone... good luck!");
                break;
        }
        
        // Display your friend's thoughts
        phoneFriendAnswerLBL1.setText("" + phoneAFriend[0] + "%");
        phoneFriendAnswerLBL2.setText("" + phoneAFriend[1] + "%");
        phoneFriendAnswerLBL3.setText("" + phoneAFriend[2] + "%");
        phoneFriendAnswerLBL4.setText("" + phoneAFriend[3] + "%");
        
        // set the results visible
        phoneFriendLBL1.setVisible(true);
        phoneFriendLBL2.setVisible(true);
        phoneFriendLBL3.setVisible(true);
        phoneFriendLBL4.setVisible(true);
        
        phoneFriendAnswerLBL1.setVisible(true);
        phoneFriendAnswerLBL2.setVisible(true);
        phoneFriendAnswerLBL3.setVisible(true);
        phoneFriendAnswerLBL4.setVisible(true);
        
        // Disable the phoneAFriendButton and set its text to USED
        phoneAFriendButton.setText("USED");
        phoneAFriendButton.setEnabled(false);
        
        phonedFriendLBL.setForeground(Color.white);
        
        // Make it easier to distinguish the answers
        answerButton1.setText("Answer 1: " + answerButton1.getText());
        answerButton2.setText("Answer 2: " + answerButton2.getText());
        answerButton3.setText("Answer 3: " + answerButton3.getText());
        answerButton4.setText("Answer 4: " + answerButton4.getText());
        
    }//GEN-LAST:event_phoneAFriendButtonActionPerformed

