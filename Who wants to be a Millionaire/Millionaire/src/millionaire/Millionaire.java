    private void logoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoButtonActionPerformed
        
        // Set the current question
        questionNum++;
        
        // Make the logo button disabled to prevent the user from 
        // skipping questions
        logoButton.setEnabled(false);
        
        // If the user gets an answer wrong at some point,
        // reset everything.
        if(questionNum == 1){
            askAudienceButton.setEnabled(true);
            askAudienceButton.setText("Ask Audience For Help");
            phoneAFriendButton.setEnabled(true);
            phoneAFriendButton.setText("Phone A Friend");
            fiftyFiftyButton.setEnabled(true);
            fiftyFiftyButton.setText("50:50");
            
            answerButton1.setEnabled(true);
            answerButton2.setEnabled(true);
            answerButton3.setEnabled(true);
            answerButton4.setEnabled(true);
            questions = null;
            questions = create.selectingTheQuestions();
            
        }
        
        // Make sure all answers are visible, this is just
        // in case the user clicked the 50:50 button.
        // This button sets half the wrong answers invisible.
        answerButton1.setVisible(true);
        answerButton2.setVisible(true);
        answerButton3.setVisible(true);
        answerButton4.setVisible(true);
        
        // Make sure the answer button's backgrounds are are reset to black.
        // Each time the user clicks an answer button whether it is right or wrong,
        // it changes that button's background color.
        answerButton1.setBackground(Color.black);
        answerButton2.setBackground(Color.black);
        answerButton3.setBackground(Color.black);
        answerButton4.setBackground(Color.black);
        
        // Hide the audience answers and the user's friend's answers
        audienceAnswer1.setVisible(false);
        audienceAnswer2.setVisible(false);
        audienceAnswer3.setVisible(false);
        audienceAnswer4.setVisible(false);
        
        audienceAnswer1LBL.setVisible(false);
        audienceAnswer2LBL.setVisible(false);
        audienceAnswer3LBL.setVisible(false);
        audienceAnswer4LBL.setVisible(false);
        
        phoneFriendLBL1.setVisible(false);
        phoneFriendLBL2.setVisible(false);
        phoneFriendLBL3.setVisible(false);
        phoneFriendLBL4.setVisible(false);
        
        phoneFriendAnswerLBL1.setVisible(false);
        phoneFriendAnswerLBL2.setVisible(false);
        phoneFriendAnswerLBL3.setVisible(false);
        phoneFriendAnswerLBL4.setVisible(false);
        
        phonedFriendLBL.setForeground(Color.black);
        // End of hiding answers
        
        // While the user hasn't answered the 15 questions needed to get to the
        // One million dollars, keep showing the questions.
        if(questionNum <= 15){
            
            // Randomize the answers in the answer buttons.
            // This will prevent the user from being able to guess the answers.
            setUpAnswers = randAnswer.nextInt(3);
            
            questionLBL.setText(questions.get(questionNum - 1).getQuestion());
        
            switch (setUpAnswers) {
                case 0:
                    answerButton1.setText(questions.get(questionNum - 1).getAnswer2());
                    answerButton2.setText(questions.get(questionNum - 1).getAnswer3());
                    answerButton3.setText(questions.get(questionNum - 1).getAnswer4());
                    answerButton4.setText(questions.get(questionNum - 1).getCorrectAnswer());
                    break;
                case 1:
                    answerButton1.setText(questions.get(questionNum - 1).getAnswer3());
                    answerButton2.setText(questions.get(questionNum - 1).getCorrectAnswer());
                    answerButton3.setText(questions.get(questionNum - 1).getAnswer2());
                    answerButton4.setText(questions.get(questionNum - 1).getAnswer4());
                    break;
                case 2:
                    answerButton1.setText(questions.get(questionNum - 1).getAnswer4());
                    answerButton2.setText(questions.get(questionNum - 1).getAnswer2());
                    answerButton3.setText(questions.get(questionNum - 1).getCorrectAnswer());
                    answerButton4.setText(questions.get(questionNum - 1).getAnswer3());
                    break;
                case 3:
                    answerButton1.setText(questions.get(questionNum - 1).getCorrectAnswer());
                    answerButton2.setText(questions.get(questionNum - 1).getAnswer4());
                    answerButton3.setText(questions.get(questionNum - 1).getAnswer3());
                    answerButton4.setText(questions.get(questionNum - 1).getAnswer2());
                    break;
                default:
                    break;
            }
            // End of randomizing the answers
            
        }
        
    }//GEN-LAST:event_logoButtonActionPerformed

